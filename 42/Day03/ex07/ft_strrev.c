char	*ft_strrev(char *str)
{
	int i;
	int j;
	int tmp;

	i = 0;
	j = 0;
	while (str[i] != '\0')
		i++;
	i--;
	while (i > j)
	{
		tmp = str[i];
		str[i] = str[j];
		str[j] = tmp;
		i--;
		j++;
	}
	i = 0;
	while (str[i] != '\0')
		i++;
	return (str);
}
