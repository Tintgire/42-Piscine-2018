int		ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb <= 1)
		return (0);
	if (nb == 2)
		return (1);
	while (nb % i != 0)
	{
		if (i < nb)
			++i;
	}
	if (nb == i)
		--i;
	if (nb % i == 0)
		return (0);
	return (1);
}
